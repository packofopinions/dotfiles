--- Theme customization ----------------

vim.cmd [[
    syntax enable
    set termguicolors
    colorscheme mountaineer

    highlight LineNr guibg=NONE guifg=grey
    highlight Visual guibg=grey guifg=black
    highlight NonText guibg=NONE guifg=grey
    highlight CursorLineNr guibg=grey guifg=black
]]
